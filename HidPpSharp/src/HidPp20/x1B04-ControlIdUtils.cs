    /// <param name="cid"></param>
    /// <returns></returns>
    public static int[] GetControlIdCodes(ControlId cid) {
        return (from i in _lookupTable
                where i.CId == cid
                select i.CIdCode).ToArray();
    }

    /// <summary>
    /// Returns all known task ids for control id
    /// </summary>
    /// <param name="cid"></param>
    /// <returns></returns>
    public static int[] GetTaskIdCodes(ControlId cid) {
        return (from i in _lookupTable
                where i.CId == cid
                select i.TaskId).ToArray();
    }

    /// <summary>
    /// Find ControlIds by code
    /// </summary>
    /// <param name="code"></param>
    /// <returns></returns>
    public static ControlId[] FindControlIdsByCode(int code) {
        return (from i in _lookupTable
                where i.CIdCode == code
                select i.CId).ToArray();
    }

    /// <summary>
    /// Find ControlIds by task id
